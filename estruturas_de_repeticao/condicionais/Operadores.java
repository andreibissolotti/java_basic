package estruturas_de_repeticao.condicionais;

public class Operadores {
    // IGUALDADES
    /*
    Igual:      x == y [ou x.equals(y)(para Strings)]
    Diferente:  x != y
    */

    // MATEMATICOS
    /*
    Soma:           +
    Diferença:      -
    Multiplicação:  *
    Divisão:        /
    Resto da div:   %
    */

    // RELACIONAIS
    /*
    Maior/menor que:        >/<  
    Maior/menor ou igual:   >=/<=
    */

    // LÓGICOS
    /* 
    E:      &&
    Ou:     ||
    */

    // INCREMENTO
    /*
    Incremento:     ++x ou x++
    Decremento:     --x ou x--

    Faz diferença se eu mandar incrementar antes ou depois de ler a variavél.
    */
}
